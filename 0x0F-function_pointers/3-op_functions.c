#include "3-calc.h"
/**
 * op_add - C function that adds two integers together
 * @a: the integer to be added
 * @b: The second integer
 * Return: The result of the calculation
 */
int op_add(int a, int b)
{
        return (a + b);
}

/**
 * op_sub - c program that substracts two integers
 * @a: The first integers to be added
 * @b: The second integers to be added
 * Return: The result of the substraction
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - C program that multiplies two integers
 * @a: The first integers to be added
 * @b: The second integers
 * Return: The result  of the calculation
 */
int op_mul(int a, int b)
{
        return (a * b);
}


/**
 * op_div - c program that divides two integers
 * @a: The first integers
 * @b: The second inegers
 * Return: The value of the multiplication
 */
int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - C program that look for the mod of an integers
 * @a: The first integers
 * @b: The second integers
 * Return: The answer of the calculation
 */
int op_mod(int a, int b)
{
        return (a % b);
}

