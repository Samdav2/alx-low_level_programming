#include "dog.h"
/**
 * init_dog - Initialise a variable type
 * @d: The pointer
 * @name: The dog name
 * @age: The dog name
 * @owner: The dog owner
 * Return: Nothing
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	struct d;

	if (d != NULL)
	{

		d->name = name;
		d->age = age;
		d->owner = owner;
	}
}
